#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	size;
	char	*ptr;
	int		i;

	size = ft_strlen(s) + 1;
	if (!(ptr = (char *)malloc(sizeof(char) * size)))
		return (NULL);
	i = 0;
	while (s[i])
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
